package Array;

class Printarray {
    static void main(String[] args) {
        int[] a = {12, 13, 17, 18};
        double[] b = {2.3, 7.6, 6.2, 9.2};
        for (int i = 0; i < a.length; i++) {
            System.out.print(a[i] + " ");
        }
        System.out.println();
        System.out.println("______________________________");
        for (int i = 0; i < b.length; i++) {
            System.out.print(b[i] + " ");
        }

    }
}
